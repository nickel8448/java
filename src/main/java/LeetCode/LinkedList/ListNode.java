package main.java.LeetCode.LinkedList;

public class ListNode {

    public int val;
    public ListNode next;

    ListNode(int x) {
        this.val = x;
    }

    int getVal() {
        return val;
    }

    void setVal(int val) {
        this.val = val;
    }

    ListNode getNext() {
        return next;
    }

    void setNext(ListNode next) {
        this.next = next;
    }
}

